// Special letter size adjustments
export const getLetterSize = (letter, baseSize) => {
  const oversizedLetters = ['ஒ', 'ஔ', 'ம', 'ழ', 'W', 'M', 'ओ', 'औ', 'म', 'श', 'ष', 'ఒ', 'ఔ', 'మ', 'శ', 'ష', 'ഒ', 'ഔ', 'മ', 'ശ', 'ഷ'];
  const undersizedLetters = ['இ', 'ஈ', 'உ', 'ஊ', 'I', 'l', 'इ', 'ई', 'उ', 'ऊ', 'ఇ', 'ఈ', 'ఉ', 'ఊ', 'ഇ', 'ഈ', 'ഉ', 'ഊ'];
  
  // English letters
  const englishCapitalLetters = 'ABCDEFGHIJKLMNOPQRSTUVWXYZ';
  const englishSmallLetters = 'abcdefghijklmnopqrstuvwxyz';
  
  // Hindi letters - check if it's a Devanagari character
  const hindiLetters = 'अआइईउऊऋएऐओऔअंअःकखगघङचछजझञटठडढणतथदधनपफबभमयरलवशषसह०१२३४५६७८९';
  
  // Telugu letters - check if it's a Telugu character
  const teluguLetters = 'అఆఇఈఉఊఋౠఌఎఏఐఒఓఔకఖగఘఙచఛజఝఞటఠడఢణతథదదధనపఫబభమయరఱలళఴవశషసహ౦౧౨౩౪౫౬౭౮౯';
  
  // Malayalam letters - check if it's a Malayalam character
  const malayalamLetters = 'അആഇഈഉഊഋഌഎഏഐഒഓഔകഖഗഘങചഛജഝഞടഠഡഢണതഥദധനപഫബഭമയരരലളഴവശഷസഹ൦൧൨൩൪൫൬൭൮൯';
  
  // Kannada letters - check if it's a Kannada character
  const kannadaLetters = 'ಅಆಇಈಉಊಋೠಌಎಏಐಒಓಔಕಖಗಘಙಚಛಜಝಞಟಠಡಢಣತಥದಧನಪಫಬಭಮಯರಱಲಳವಶಷಸಹ೦೧೨೩೪೫೬೭೮೯';
  
  if (englishCapitalLetters.includes(letter)) {
    return Math.round(baseSize * 0.8); // Comfortable size for Caveat capitals
  }
  
  if (englishSmallLetters.includes(letter)) {
    return Math.round(baseSize * 1.125); // Increased by 0.5x (0.75 * 1.5 = 1.125) for Caveat lowercase
  }
  
  if (hindiLetters.includes(letter)) {
    return Math.round(baseSize * 0.9); // Slightly smaller for Devanagari script
  }
  
  if (teluguLetters.includes(letter)) {
    return Math.round(baseSize * 0.7); // Much smaller for Telugu script to fit drawing area
  }
  
  if (malayalamLetters.includes(letter)) {
    return Math.round(baseSize * 0.6); // Even smaller for Malayalam script to fit drawing area
  }
  
  if (kannadaLetters.includes(letter)) {
    return Math.round(baseSize * 0.7); // Appropriate size for Kannada script to fit drawing area
  }
  
  if (oversizedLetters.includes(letter)) {
    return Math.round(baseSize * 0.85); // 15% smaller
  }
  
  if (undersizedLetters.includes(letter)) {
    return Math.round(baseSize * 1.1); // 10% larger
  }
  
  return baseSize;
};

